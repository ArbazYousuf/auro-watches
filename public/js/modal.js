//                  Modal JavaScript for Rolex

// Get The Modal 
var modalrolex = document.getElementById('modalrolex');

// Get The Button that opens the modal 
var openbtn = document.getElementById('btnrolex');

//get the <Span> element that closes the modal
    var span = document.getElementById('close')[0];

// When the user clicks on the button, open the modal 
    function clkrolex() {
        document.getElementById('modalrolex').style.display = "block";
    }
// When the user clicks on <span> (X),close the modal
    function clzrolex(){
        document.getElementById('modalrolex').style.display = "none";
    }
// When the user clicks anywhere outside of the modal,close it 
    window.onclick = function(event){
        if(event.target == modalrolex) {
            document.getElementById('modalrolex').style.display = "none";
        }
    }
